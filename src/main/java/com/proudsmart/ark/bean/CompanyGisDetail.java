package com.proudsmart.ark.bean;

public class CompanyGisDetail {
	private double pointx;
	private double pointy;
	private String city;
	private String district;
	private String detailStringContent;
	
	public double getPointx() {
		return pointx;
	}
	public void setPointx(double pointx) {
		this.pointx = pointx;
	}
	public double getPointy() {
		return pointy;
	}
	public void setPointy(double pointy) {
		this.pointy = pointy;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getDistrict() {
		return district;
	}
	public void setDistrict(String district) {
		this.district = district;
	}
	public String getDetailStringContent() {
		return detailStringContent;
	}
	public void setDetailStringContent(String detailStringContent) {
		this.detailStringContent = detailStringContent;
	}
	
}
